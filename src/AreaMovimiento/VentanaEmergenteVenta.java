    private void btnComprarActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_btnComprarActionPerformed
        // TODO add your handling code here:
        try {
            int cuantoQuiere = Integer.parseInt(this.txtCantidad.getText());
            int precio = Integer.parseInt(this.txtPrecio.getText());
            int cantidadDisponible = this.compra.getCantidad();
            Producto producto = this.compra.getProducto();
            Puesto puesto = this.compra.getPuesto();
            if (cuantoQuiere <= cantidadDisponible) {
                int numero = this.ventas.registrarVenta(cuantoQuiere, precio,
                        producto, puesto);
                JOptionPane.showMessageDialog(null, "Venta exitosa. Recuerde gurdar su"
                        + " nÃºmero de venta " + numero, "Felicidades",
                        JOptionPane.PLAIN_MESSAGE);
                this.registroCompra.reducirCantidadCompra(cuantoQuiere, producto, puesto);
                //this.generarArchivo.ObjetoaPantalla();
            } else {
                mensajeError("La cantidad disponible para compra es de "
                        + cantidadDisponible);
            }
        } catch (InputMismatchException e) {
            mensajeError("Debe ingresar precio y cantidad en formato numerico");
        }
        this.limpiarCampos();
    }// GEN-LAST:event_btnComprarActionPerformed


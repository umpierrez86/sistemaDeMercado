    public void ObjetosPantalla() {
        Producto[] arrProductos = this.productosDisponibles.getListaProductos().toArray(
                new Producto[this.productosDisponibles.getListaProductos().size()]);
        this.listProductosAdd.setListData(arrProductos);
    }

    @Override
    public void update(Observable observado, Object arg) {
        if (arg instanceof Producto) {
            ObjetosPantalla();
        }
    }

    private void btnAgregarProductoActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_btnAgregarProductoActionPerformed
        try {
            Producto seleccion = (Producto) listProductosAdd.getSelectedValue();
            if (seleccion != null && !this.listaProductosSeleccionados.contains(seleccion)) {
                this.listaProductosSeleccionados.add(seleccion);
                Producto[] arrayProductos = this.listaProductosSeleccionados.toArray(
                        new Producto[this.listaProductosSeleccionados.size()]);
                this.listProductos.setListData(arrayProductos);
            } else {
                throw new Exception("Producto ya agregado");
            }
        } catch (Exception e) {
            this.mostrarError(e.getMessage());
        }

    }// GEN-LAST:event_btnAgregarProductoActionPerformed

    private void btnAltaActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_btnAltaActionPerformed
        // registroMayoristas = new RegistroMayorista();
        try {

            if (this.txtfRUT.getText().equals("") || this.txtfNombre.getText().equals("")
                    || this.txtfDireccion.getText().equals("")) {

                throw new Exception("Debe completar todos los datos");
            }
            if (registroMayoristas.validarRut(this.txtfRUT.getText())) {
                if (!registroMayoristas.existeRut(this.txtfRUT.getText())) {
                    String rut = this.txtfRUT.getText();
                    String nombre = this.txtfNombre.getText();
                    String direccion = this.txtfDireccion.getText();
                    if (!this.listaProductosSeleccionados.isEmpty()) {
                        registroMayoristas.RegistrarMayorista(rut, nombre, direccion,
                                listaProductosSeleccionados);
                        JOptionPane.showMessageDialog(this,
                                "Mayorista ingresado correctamente",
                                "Exito", JOptionPane.INFORMATION_MESSAGE);
                        this.txtfRUT.setText("");
                        this.txtfNombre.setText("");
                        this.txtfDireccion.setText("");
                        Producto[] arrayVacio = new Producto[1];
                        this.listProductos.setListData(arrayVacio);
                        this.listaProductosSeleccionados = new ArrayList<>();
                    } else {
                        mostrarError("Error: debe al menos ingresar un producto");
                    }
                } else {
                    mostrarError("Error: ese RUT ya pertenece a un mayorista.");
                }
            } else {
                mostrarError(
                        "Error: Rut invalido, debe ingresar 8 digitos numericos y un digito verificador alfanumerico separado de un caracter especial");
            }
        } catch (Exception e) {
            mostrarError(e.getMessage());
        }
    }// GEN-LAST:event_btnAltaActionPerformed

    public void mostrarError(String mensaje) {
        JOptionPane.showMessageDialog(null, mensaje, "Error", JOptionPane.ERROR_MESSAGE);
    }

    private void btnQuitarProductoActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_btnQuitarProductoActionPerformed
        try {
            Producto seleccion = (Producto) listProductos.getSelectedValue();
            if (seleccion != null) {
                this.listaProductosSeleccionados.remove(seleccion);
                Producto[] arrayProductos = this.listaProductosSeleccionados.toArray(
                        new Producto[this.listaProductosSeleccionados.size()]);
                this.listProductos.setListData(arrayProductos);
            } else {
                throw new Exception("Debe seleccinar un producto");
            }
        } catch (Exception e) {
            mostrarError(e.getMessage());
        }
    }// GEN-LAST:event_btnQuitarProductoActionPerformed


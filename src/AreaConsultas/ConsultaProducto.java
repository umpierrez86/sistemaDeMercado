    public void mostrarListas() {
        Iterator<Puesto> itMaximo = this.puestoPrecioMaximo.iterator();
        Iterator<Puesto> itMinimo = this.puestosPrecioMinimo.iterator();
        DefaultListModel<Puesto> modeloMinimo = new DefaultListModel<>();
        DefaultListModel<Puesto> modeloMaximo = new DefaultListModel<>();

        while (itMaximo.hasNext()) {
            Puesto puesto = itMaximo.next();
            modeloMaximo.addElement(puesto);
        }

        while (itMinimo.hasNext()) {
            Puesto puesto = itMinimo.next();
            modeloMinimo.addElement(puesto);
        }

        this.lstPrecioMaximo.setModel(modeloMaximo);
        this.lstPrecioMinimo.setModel(modeloMinimo);
    }

    public void mostrarProducto() {
        ArrayList<Producto> listaProductos = this.productos.getListaProductos();
        if (!listaProductos.isEmpty()) {
            Producto unProducto = listaProductos.get(this.indiceActual);
            this.txtNombre.setText(unProducto.getNombre());
            this.txtDescripcion.setText(unProducto.getDescripcion());
            this.txtTipo.setText(unProducto.getTipo());
            this.txtVenta.setText(unProducto.getFormaVenta());
            ImageIcon icon = new ImageIcon(unProducto.getFoto());
            this.lblFoto.setIcon(icon);
            this.totalVendidos = this.ventas.totalVendidoProducto();
            this.totalCompras = this.compras.totalCompradoProducto();
            if (this.totalVendidos.get(unProducto) == null) {
                this.txtTotalVendido.setText("0");
            } else {
                this.txtTotalVendido.setText("" + this.totalVendidos.get(unProducto));
            }
            if (this.totalCompras.get(unProducto) == null) {
                this.txtTotalComprado.setText("0");
            } else {
                this.txtTotalComprado.setText("" + this.totalCompras.get(unProducto));
            }
            this.cantidadComprada = this.compras.cantidadComprada(unProducto);
            this.cantidadVendida = this.ventas.cantidadVendida(unProducto);
            this.txtPrecioMinimo.setText("" + this.cantidadVendida[2]);
            this.txtPrecioMaximo.setText("" + this.cantidadVendida[3]);
            int kiloComprado = this.cantidadComprada[0];
            int unidadComprado = this.cantidadComprada[1];
            int kiloVendido = this.cantidadVendida[0];
            int unidadVendida = this.cantidadVendida[1];
            this.txtCantidadComprado.setText(unidadComprado + "/" + kiloComprado);
            this.txtCantidadVendido.setText(unidadVendida + "/" + kiloVendido);
            ArrayList<Puesto>[] arrayListaPuestos = this.ventas.puestosPrecioMinimoyMaximo(
                    this.cantidadVendida[2], this.cantidadVendida[3], unProducto);
            this.puestoPrecioMaximo = arrayListaPuestos[1];
            this.puestosPrecioMinimo = arrayListaPuestos[0];
            this.mostrarListas();

        }
    }


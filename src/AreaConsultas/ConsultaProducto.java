    private void btnAtrasActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_btnAtrasActionPerformed
        // Verifica que el usuario no pueda retorceder si esta en el primero
        if (this.indiceActual > 0) {
            this.indiceActual--;
            mostrarProducto();
        }
    }// GEN-LAST:event_btnAtrasActionPerformed

    private void btnSiguienteActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_btnSiguienteActionPerformed
        // Verifica que el usuario no pueda pasar a otro producto si esta en el ultimo
        int maximo = this.productos.getListaProductos().size();
        if (this.indiceActual < (maximo - 1)) {
            this.indiceActual++;
            mostrarProducto();
        }
    }// GEN-LAST:event_btnSiguienteActionPerformed

